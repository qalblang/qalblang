def اطبع(*متغيرات, انفصال=' ', نهاية='\n', ملف=None):
    num_dict = {"0": "٠", "1": "١", "2": "٢", "3": "٣", "4": "٤", "5": "٥", "6": "٦", "7": "٧", "8": "٨", "9": "٩"}
    رسالة = ""
    for متغير in متغيرات:
        متغير = str(متغير)
        for num in num_dict.keys():
            if num in متغير:
                متغير = متغير.replace(num, num_dict[num])
        رسالة += متغير + انفصال

    print(رسالة, sep=انفصال, end=نهاية, file=ملف)


